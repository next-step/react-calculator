export class Calculator {
    static sum(num1, num2) {
        return num1 + num2;
    }

    static substract(num1, num2) {
        return num1 - num2;
    }

    static multiple(num1, num2) {
        return num1 * num2;
    }

    static divide(num1, num2) {
        return Math.floor(num1 / num2);
    }
}

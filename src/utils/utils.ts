export function sum(num1: number, num2: number) {
  return num1 + num2;
}

export function subtract(num1: number, num2: number) {
  return num1 - num2;
}

export function multiple(num1: number, num2: number) {
  return num1 * num2;
}

export function divide(num1: number, num2: number) {
  return num1 / num2;
}
